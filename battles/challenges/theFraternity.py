def fab2cs(f):
    out = []
    for line in f:
        pl = ""
        for c in line:
            if c == 0:
                pl += "-"
            else:
                pl += "|"
        out.append(pl)
    s = str(out)
    s = s.replace("'", '"')
    return s

def fab2list(f):
    out = []
    for line in f:
        pl = ""
        for c in line:
            if c == 0:
                pl += "-"
            else:
                pl += "|"
        out.append(pl)
    return out

def r32():
    return randint(2, 27)


def findspots():
    spots = 0
    loc = []
    while spots < 16:
        rx = r32()
        ry = r32()

        collision = False
        # check spots 2-3 squares in either direction
        for i in range(rx-3, rx+4):
            for j in range(ry-3, ry+4):
                if (i, j)  in loc:
                    collision = True
                    break
            if collision:
                break
        else:
            spots += 1
            loc.append((rx, ry))

    for i in range(32):
        line = ""
        for j in range(32):
            if (i, j) in loc:
                line += "o"
            else:
                line += "."
        print(line)
        
    return sorted(loc)


def alter(f, val, x, y):
    if f[x][y] == val:
        f[x][y+1] = val
    else:
        f[x][y] = val

        
def altseq(initials):
    return "0" + bin(ord(initials[0]))[2:] + "0" + bin(ord(initials[1]))[2:]


