def lcdMinNumber(n):
    segs = [6, 2, 5, 5, 4, 5, 6, 3, 7, 6]
    for i in range(2999999):
        digs = str(i)
        tot = sum(segs[int(d)] for d in digs)
        if tot == n:
            return str(i)
    return "phail"

