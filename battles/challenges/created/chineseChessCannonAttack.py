# See if there is a cannon

def chineseChessCannonAttack(board):
    rows = len(board)
    cols = len(board[0])

    # find location of general
    for r in range(rows):
        for c in range(cols):
            if board[r][c] == 'g':
                gr, gc = (r, c)


    # up
    found = 0
    if gr > 0:
        for r in range(gr - 1, -1, -1):
            if board[r][gc] != '.':
                found += 1
            if board[r][gc] == 'c' and found == 2:
                return [r, gc]

    # left
    found = 0
    if gc > 0:
        for c in range(gc - 1, -1, -1):
            if board[gr][c] != '.':
                found += 1
            if board[gr][c] == 'c' and found == 2:
                return [gr, c]

    # right
    found = 0
    if gc < cols - 1:
        for c in range(gc + 1, cols):
            if board[gr][c] != '.':
                found += 1
            if board[gr][c] == 'c' and found == 2:
                return [gr, c]

    # bottom
    found = 0
    if gr < rows - 1:
        for r in range(gr + 1, rows):
            if board[r][gc] != '.':
                found += 1
            if board[r][gc] == 'c' and found == 2:
                return [r, gc]
            
    return [-1, -1]

